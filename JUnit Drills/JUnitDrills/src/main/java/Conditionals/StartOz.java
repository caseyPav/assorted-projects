public class StartOz {
    public String StartOz(String str) {
        String s;
        if((str.contains("o")&& str.contains("z"))&& (str.indexOf("o")< str.indexOf("z"))){return s= "oz";}
        else if(str.contains("o")){return s= "o";}
        else{return s="";}

}
    
}
